
public class Exam1 {
	public static void main(String[] args) {
		// 1. 정수 저장
		int a;	// 변수만들기
		a = 5;	// 데이터 저장
		System.out.println(a);  // 저장된 데이터 확인
		System.out.println("----------------");
		
		// 2. 실수 저장
		double b;
		b = 5.5;
		System.out.println(b);  // 저장된 데이터 확인
		System.out.println("----------------");
		
		// 3. 문자 저장
		char c = 'A';
		System.out.println(c);  // 저장된 데이터 확인
		System.out.println("----------------");
		
		// 4. boolean 값 저장
		boolean d = true;
		System.out.println(d);  // 저장된 데이터 확인
		System.out.println("----------------");
	}
}







